def get_character_prompt(character: str) -> str:
    """캐릭터 이름으로 프롬프트를 가져옵니다."""
    return CHARACTER_PROMPTS.get(character, CHARACTER_PROMPTS["강아지"])

def get_available_characters() -> list:
    """사용 가능한 캐릭터 목록을 반환합니다."""
    return list(CHARACTER_PROMPTS.keys())
